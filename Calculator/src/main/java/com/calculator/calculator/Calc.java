package com.calculator.calculator;

public class Calc {

    public void sum(float a, float b) {
        System.out.println("value sum -> " + (a + b));
    }

    public void substract(float a, float b) {
        System.out.println("value substract -> " + (a - b));
    }

    public void multiplication(float a, float b) {
        System.out.println("value multiplication -> " + (a * b));
    }

    public void divide(float a, float b) {
        System.out.println("value divide -> " + (a / b));
    }
}
